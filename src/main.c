#include "cub.h"

t_cub	*mlx_windows(int width, int height, char *title)
{
	t_cub	*cub;

	cub = malloc(sizeof(t_cub));
	if (!cub)
		return (NULL);
	cub->mlx = mlx_init();
	if (!cub->mlx)
	{
		free(cub);
		return (NULL);
	}
	cub->win = mlx_new_window(cub->mlx, width, height, title);
	if (!cub->win)
	{
		free(cub);
		return (NULL);
	}
	return (cub);
}

